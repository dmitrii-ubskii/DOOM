#[unsafe(no_mangle)]
pub extern "C" fn SlopeDiv(num: usize, den: usize) -> usize {
	if den < 512 {
		return SLOPERANGE;
	}
	let ans = (num << 3) / (den >> 8);
	usize::min(ans, SLOPERANGE)
}

